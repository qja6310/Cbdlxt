package cn.com.newloading.bean;

public class Params {

	private String id;
	private String targetName;
	private String weight;
	private String scoreWay;
	private String targetValue;
	private String tolerance;
	private String linkModelId;
	private String score;
	public Params() {
		super();
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTargetName() {
		return targetName;
	}
	public void setTargetName(String targetName) {
		this.targetName = targetName;
	}
	public String getWeight() {
		return weight;
	}
	public void setWeight(String weight) {
		this.weight = weight;
	}
	public String getScoreWay() {
		return scoreWay;
	}
	public void setScoreWay(String scoreWay) {
		this.scoreWay = scoreWay;
	}
	public String getTargetValue() {
		return targetValue;
	}
	public void setTargetValue(String targetValue) {
		this.targetValue = targetValue;
	}
	public String getTolerance() {
		return tolerance;
	}
	public void setTolerance(String tolerance) {
		this.tolerance = tolerance;
	}
	public String getLinkModelId() {
		return linkModelId;
	}
	public void setLinkModelId(String linkModelId) {
		this.linkModelId = linkModelId;
	}
	public String getScore() {
		return score;
	}
	public void setScore(String score) {
		this.score = score;
	}
	
	
	
}
