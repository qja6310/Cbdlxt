package cn.com.newloading.bean;

public class Project {

	private String id;
	private String pgNum;
	private String pgName;
	private String jczbName;
	private String jczbNum;
	private String pgDesc;
	private String createTime;
	private String userId;
	
	private String userName;

	public Project() {
		super();
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPgNum() {
		return pgNum;
	}

	public void setPgNum(String pgNum) {
		this.pgNum = pgNum;
	}

	public String getPgName() {
		return pgName;
	}

	public void setPgName(String pgName) {
		this.pgName = pgName;
	}

	public String getJczbName() {
		return jczbName;
	}

	public void setJczbName(String jczbName) {
		this.jczbName = jczbName;
	}

	public String getJczbNum() {
		return jczbNum;
	}

	public void setJczbNum(String jczbNum) {
		this.jczbNum = jczbNum;
	}

	public String getPgDesc() {
		return pgDesc;
	}

	public void setPgDesc(String pgDesc) {
		this.pgDesc = pgDesc;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}
	
	
}
