package cn.com.newloading.bean;

public class LinkModel {

	private String id;
	private String jcmId;
	private String taskModelId;
	private String projectId;
	private String userId;
	private String createTime;
	public LinkModel() {
		super();
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getJcmId() {
		return jcmId;
	}
	public void setJcmId(String jcmId) {
		this.jcmId = jcmId;
	}
	public String getTaskModelId() {
		return taskModelId;
	}
	public void setTaskModelId(String taskModelId) {
		this.taskModelId = taskModelId;
	}
	public String getProjectId() {
		return projectId;
	}
	public void setProjectId(String projectId) {
		this.projectId = projectId;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	
	
	
}
