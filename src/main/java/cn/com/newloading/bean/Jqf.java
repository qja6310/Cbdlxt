package cn.com.newloading.bean;

public class Jqf {

	private String id;
	private String jcmId;
	private String qfunCode;
	private String status;
	private String calResult;
	
	private String funName;
	private String pid;
	
	public Jqf() {
		
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getJcmId() {
		return jcmId;
	}

	public void setJcmId(String jcmId) {
		this.jcmId = jcmId;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getFunName() {
		return funName;
	}

	public void setFunName(String funName) {
		this.funName = funName;
	}

	public String getCalResult() {
		return calResult;
	}

	public void setCalResult(String calResult) {
		this.calResult = calResult;
	}

	public String getQfunCode() {
		return qfunCode;
	}

	public void setQfunCode(String qfunCode) {
		this.qfunCode = qfunCode;
	}

	public String getPid() {
		return pid;
	}

	public void setPid(String pid) {
		this.pid = pid;
	}
	
	
}
