package cn.com.newloading.bean;

public class Jsf {

	private String id;
	private String jcmId;
	private String funCode;
	private String status;
	private String calResult;
	
	private String funName;
	
	public Jsf() {
		
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getJcmId() {
		return jcmId;
	}

	public void setJcmId(String jcmId) {
		this.jcmId = jcmId;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getFunCode() {
		return funCode;
	}

	public void setFunCode(String funCode) {
		this.funCode = funCode;
	}

	public String getFunName() {
		return funName;
	}

	public void setFunName(String funName) {
		this.funName = funName;
	}

	public String getCalResult() {
		return calResult;
	}

	public void setCalResult(String calResult) {
		this.calResult = calResult;
	}
	
	
}
